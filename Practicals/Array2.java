
public class Array2 {
    public static void main(String[] args) {
        /*Create array*/
        int[] ten = {10,20,20,40,50,60,70,80,90,100};
        int sum = 0;
        /*Initialize array*/
        for (int i = 0; i < 9; i++) {
            sum += ten[i];
        }
    System.out.println("The sum is : "+sum);
}}
